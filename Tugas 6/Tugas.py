#OVERLOADING

class ComputerPart():
  def __init__(self,nama,harga,kualitas): 
    self.nama = nama 
    self.harga = harga
    self.kualitas = kualitas 
  def data(self):
    print('Nama :',self.nama,', Harga :',self.harga,'Kualitas :',self.kualitas)
  
# Method Overloading
  def stok(self,stok): 
    if stok >= 5:
      print("Stok Masih Ready Banyak")
    else:
      print("Stok Hanya Tersedia Terbatas!")

