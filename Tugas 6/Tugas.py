class ComputerPart():
  def __init__(self,nama,harga,kualitas): 
    self.nama = nama 
    self.harga = harga
    self.kualitas = kualitas

  def data(self): # Method Overriding
    print('Nama :',self.nama,', Harga :',self.harga,', Kualitas :',self.kualitas)

class CentralProcessingUnit(ComputerPart):
  def __init__(self,nama,harga,kualitas): 
    self.nama = nama
    self.harga = harga
    self.kualitas = kualitas

  def data(self): # Method Overriding
    print('Nama :',self.nama,', Harga :',self.harga,', Kualitas :',self.kualitas)

